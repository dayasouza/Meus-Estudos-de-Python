# Escreva as seguintes expressões algébricas em linguagem Python:

# O somatório dos 5 primeiros números inteiros e positivos

soma = (1 + 2 + 3 + 4 + 5)
print('A soma dos 5 primeiros números inteiros e positivos é: {}.' .format(soma))

# A média entre 23, 19 e 31

m = (23+ 19+ 31) /3
print('A média entre os números 23, 19 e 31 é: {}.' .format(m))

# O número de vezes que 73 cabe em 403

vezes = 403 // 73
print('O número 73 cabe {}x dentro do número 403.' .format(vezes))

# A sobra quando 403 é dividido por 73

sobra = 403 % 73
print('A sobra de 403 dividido por 73 é: {}.' .format(sobra))

# 2 elevado à 10ª potência

potencia = 2**10
print('2 elevado a 10ª é: {}.' . format(potencia))

# O valor absoluto da diferença entre 54 e 57
#abs é a função que usamos para ter o resultado do valor absoluto ignorando o sinal

valor_abs = abs(54-57)
print('O valor absoluto da diferença entre 54 e 57 é: {}.' .format(valor_abs))

# O menor valor entre 34, 29 e 31

menor_valor = min(34, 29,31)
print('O menor valor entre 34, 29 e 31 é: {}.' .format(menor_valor))
